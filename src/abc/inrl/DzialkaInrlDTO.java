package abc.inrl;

public class DzialkaInrlDTO {
    protected String numery_dzialek;
    protected String identyfikatory_geodezyjne_budynkow;
    protected String numery_kw;

    public DzialkaInrlDTO() {
    }

    public String getNumery_dzialek() {
        return numery_dzialek;
    }

    public void setNumery_dzialek(String numery_dzialek) {
        this.numery_dzialek = numery_dzialek;
    }

    public String getIdentyfikatory_geodezyjne_budynkow() {
        return identyfikatory_geodezyjne_budynkow;
    }

    public void setIdentyfikatory_geodezyjne_budynkow(String identyfikatory_geodezyjne_budynkow) {
        this.identyfikatory_geodezyjne_budynkow = identyfikatory_geodezyjne_budynkow;
    }

    public String getNumery_kw() {
        return numery_kw;
    }

    public void setNumery_kw(String numery_kw) {
        this.numery_kw = numery_kw;
    }
}
