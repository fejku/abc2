package abc.inrl;

public class ImieNazwiskoDTO {
    protected String imie;
    protected String imie_drugie;
    protected String nazwisko;

    public ImieNazwiskoDTO() {
    }

    public String getImie() {
        return imie;
    }

    public void setImie(String imie) {
        this.imie = imie;
    }

    public String getImie_drugie() {
        return imie_drugie;
    }

    public void setImie_drugie(String imie_drugie) {
        this.imie_drugie = imie_drugie;
    }

    public String getNazwisko() {
        return nazwisko;
    }

    public void setNazwisko(String nazwisko) {
        this.nazwisko = nazwisko;
    }
}
